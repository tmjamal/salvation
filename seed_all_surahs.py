def seed_surahs():
    with app.app_context():
        # Delete existing surahs (but keep verses for surah 1)
        existing = {s.number: s for s in Surah.query.all()}
        
        for s in ALL_SURAHS:
            num, ar, ml, en, verses, rev_type = s
            if num in existing:
                # Update existing
                existing[num].name_arabic = ar
                existing[num].name_malayalam = ml
                existing[num].name_english = en
                existing[num].total_verses = verses
                existing[num].revelation_type = rev_type
            else:
                # Add new
                db.session.add(Surah(
                    number=num, name_arabic=ar, name_malayalam=ml,
                    name_english=en, total_verses=verses, revelation_type=rev_type
                ))
        
        db.session.commit()
        count = Surah.query.count()
        print(f"Done! Total surahs in database: {count}")

if __name__ == '__main__':
    seed_surahs()
